"use client";

/**
 * MediSport Phase 7 — AthleteProgressPdf (shared, DB-backed)
 *
 * A self-contained "Export PDF" control that aggregates the athlete's full
 * MediSport progress — body composition, biological age, recent activity, and
 * the latest lab report — into a branded, printable PDF report.
 *
 * Data is pulled from the existing protected endpoints:
 *   - GET /api/sport?action=my-body-measurements
 *   - GET /api/sport?action=my-bio-age
 *   - GET /api/sport?action=my-activities
 *   - GET /api/sport?action=my-lab-results
 *
 * It is used by both the standalone `(sport)` trainee pages and (optionally)
 * the integrated `/medisport` module — true mirroring. The athlete name is read
 * from the client session; an optional `athleteName` prop lets a coach export a
 * report on a linked trainee's behalf without a session lookup.
 *
 * PDF rendering reuses the project's proven jsPDF + html2canvas pattern
 * (oklch/lab color sanitization before capture) used in MediLab.
 */

import * as React from "react";
import { useLocale } from "next-intl";
import { toast } from "sonner";
import { FileDown, Loader2 } from "lucide-react";
import { Button } from "@/components/ui/button";
import { useSession } from "@/lib/auth-client";

// ── Data shapes (mirrors the API responses) ──────────────────────────────────
type Measurement = {
  id: string;
  measuredAt: string;
  weightKg: string | null;
  bodyFatPct: string | null;
  muscleMassKg: string | null;
  waterPct: string | null;
  waistCm: string | null;
  note: string | null;
};
type BodyComparison = {
  from: string;
  to: string;
  weightKg: number | null;
  bodyFatPct: number | null;
  muscleMassKg: number | null;
  waistCm: number | null;
} | null;
type BioAgeRecord = {
  id: string;
  chronologicalAge: string;
  biologicalAge: string;
  ageDelta: string;
  percentile: number | null;
  classification: string | null;
  createdAt: string;
};
type Activity = {
  id: string;
  activityType: string;
  startedAt: string;
  durationSec: number;
  distanceMeters: string;
  caloriesBurned: string;
};
type LabMarker = {
  name: string;
  category: string;
  value: number;
  unit: string;
  athleteMin?: number;
  athleteMax?: number;
};
type LabReport = {
  id: string;
  title: string;
  reportDate: string;
  seasonPhase: string | null;
  markers: LabMarker[];
  note: string | null;
};

type Bundle = {
  body: Measurement[];
  bodyComparison: BodyComparison;
  bioAge: BioAgeRecord[];
  activities: Activity[];
  labs: LabReport[];
};

