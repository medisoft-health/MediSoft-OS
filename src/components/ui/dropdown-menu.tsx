export function DropdownMenuShortcut({
  className,
  ...props
}: React.HTMLAttributes<HTMLSpanElement>) {
  return (
    <span
      className={cn(
        "ml-auto text-xs tracking-widest text-[color:var(--color-muted-foreground)]",
        className,
      )}
      {...props}
    />
  );
}
